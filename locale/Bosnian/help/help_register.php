<h3><!-- language -->"User Registration"</h3>
To register with Owl you need to fill in the following fields.
<table  style="width: 100%; text-align: left;" border="0" cellpadding="2" cellspacing="2">
<tr>
 <td width ="10%"><i><!-- language -->"Type the 5 characters"</i></td>
 <td>If activated you see this field together with an image next to it. 
 Try to identify the five characters displayed in the image an enter them into the field. 
 This is a security feature. If you cannot identify the characters 
 just push the reload button of your browser and a new image will be created.</td>
</tr>
<tr>
 <td width ="10%"><i><!-- language -->"Full Name"</i></td>
 <td>Enter your full name, e.g. Doe John.</td>
</tr>
<tr>
 <td><i><!-- language -->"Username"</i></td>
 <td>Enter your preferred username, e.g. john_doe. 
 Later, you can login to Owl using this name.</td>
</tr>
<tr>
 <td><i><!-- language -->"E-Mail Address"</i></td>
 <td>Enter your e-mail address into this field, e.g. john_doe@example.com. 
 Note that Owl will send you your password to this e-mail address. 
 So, be sure you enter the correct address.</td>
</tr>
</table>
Then push the button <!-- language -->"Register" to register with Owl. 
After that an e-mail with your password will be sent to you.


<a name="pw"></a>
<h3><!-- language -->"Show forgot password link"</h3>
If you forgot your password Owl will send you a new one to your e-mail address. 
Enter your username into the field and push the button <!-- language -->"Send Password". 
Your username is the name you use to log into Owl. It is not your full name.


<a name="rm"></a>
<h3><!-- language -->"Remember me"</h3>
If you check this box you do not have to enter your login information anymore. 
Instead everytime you browse to Owl you will be redirected directly to the file browser. 
To use this feature you need to activate cookies in your browser.
<p>Do not use this feature on public computers!
</p>


<a name="chpw"></a>
<h3><!-- language -->"CHANGE PASSWORD"</h3>
If this window appears you have to change your password. 
Thus, enter your old password into the field <!-- language -->"Old Password". 
Then choose a new password and enter it into the fields <!-- language -->"New Password" 
and <!-- language -->"Confirm New Password". 
